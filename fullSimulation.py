    axes[1].set_xlabel('Network Size (nodes)', fontsize=11)
    axes[1].set_ylabel('Avg Path Loss (%)', fontsize=11)
    axes[1].set_title('Scalability: Path Quality', fontweight='bold')
    axes[1].legend()
    axes[1].grid(alpha=0.3)

    fig.tight_layout()
    fig.savefig('figures/fig06_scalability.png', dpi=150)
    plt.close()
    print("  Saved: figures/fig06_scalability.png")


