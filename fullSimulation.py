def plot_topology(G):
    fig, ax = plt.subplots(figsize=(12, 9))
    pos = nx.spring_layout(G, seed=42, k=1.8)
    bw_colors = {10: '#E74C3C', 100: '#3498DB', 1000: '#2ECC71'}
    edge_colors = [bw_colors[G[u][v]['bandwidth']] for u, v in G.edges()]
    edge_widths = [0.8 + 3 * G[u][v]['bandwidth'] / 1000 for u, v in G.edges()]

    nx.draw_networkx_nodes(G, pos, node_color='#2C3E50', node_size=450, ax=ax)
    nx.draw_networkx_labels(G, pos, font_color='white', font_size=9,
                            font_weight='bold', ax=ax)
    nx.draw_networkx_edges(G, pos, edge_color=edge_colors,
                           width=edge_widths, alpha=0.7, ax=ax)

    import matplotlib.patches as mpatches
    patches = [
        mpatches.Patch(color='#2ECC71', label='1 Gbps (fibre)'),
        mpatches.Patch(color='#3498DB', label='100 Mbps (copper)'),
        mpatches.Patch(color='#E74C3C', label='10 Mbps (wireless/legacy)'),
    ]
    ax.legend(handles=patches, fontsize=11)
    ax.set_title(
        f'Heterogeneous Network Topology  '
        f'({G.number_of_nodes()} nodes, {G.number_of_edges()} links)',
        fontsize=14, fontweight='bold')
    ax.axis('off')
    fig.tight_layout()
    fig.savefig('figures/fig05_topology.png', dpi=150)
    plt.close()
    print("  Saved: figures/fig05_topology.png")


def plot_scalability(scale_results):
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    sizes = [r['size'] for r in scale_results]

    for proto in ['OSPF', 'QAA-OSPF', 'EQA-OSPF']:
        times, valid_sizes = [], []
        for r in scale_results:
            if proto in r and not r[proto].get('skipped', False):
                times.append(r[proto]['comp_time'])
                valid_sizes.append(r['size'])
        if times:
            axes[0].plot(valid_sizes, times, 'o-', color=COLORS[proto],
                         linewidth=2.5, markersize=7, label=proto)

    axes[0].set_xlabel('Network Size (nodes)', fontsize=11)
    axes[0].set_ylabel('Route Computation Time (ms)', fontsize=11)
    axes[0].set_title('Scalability: Computation Time', fontweight='bold')
    axes[0].legend()
    axes[0].grid(alpha=0.3)

    for proto in ['OSPF', 'QAA-OSPF', 'EQA-OSPF']:
        losses, valid_sizes = [], []
        for r in scale_results:
            if proto in r and not r[proto].get('skipped', False):
                losses.append(r[proto]['quality']['avg_loss'])
                valid_sizes.append(r['size'])
