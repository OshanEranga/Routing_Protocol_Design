# 1. NETWORK TOPOLOGY
#    Increased to 20 nodes with 0.8 extra-edge ratio for
#    richer path diversity — the condition where QoS-aware
#    multi-component metrics differentiate most from hop-count
#    or single-metric approaches.
# ════════════════════════════════════════════════════════════

def build_topology(n_nodes=20, seed=42):
    """Build connected graph with realistic correlated link properties."""
    random.seed(seed)
    G = nx.Graph()
    G.add_nodes_from(range(n_nodes))

    # Spanning tree for guaranteed connectivity
    nodes = list(range(n_nodes))
    random.shuffle(nodes)
    for i in range(1, len(nodes)):
        G.add_edge(nodes[i-1], nodes[i], **_rand_link())

    # Extra edges — 0.8 × n_nodes gives good path diversity
    extra = int(n_nodes * 0.8)
    attempts = 0
    while extra > 0 and attempts < 800:
        u, v = random.randint(0, n_nodes-1), random.randint(0, n_nodes-1)
        if u != v and not G.has_edge(u, v):
            G.add_edge(u, v, **_rand_link())
            extra -= 1
        attempts += 1

    print(f"  Topology: {G.number_of_nodes()} nodes, {G.number_of_edges()} edges")
    bw_counts = {10: 0, 100: 0, 1000: 0}
    for u, v, d in G.edges(data=True):
        bw_counts[d['bandwidth']] += 1
    print(f"  Link mix: {bw_counts[1000]}× 1 Gbps, "
          f"{bw_counts[100]}× 100 Mbps, {bw_counts[10]}× 10 Mbps")
    avg_delay = np.mean([d['delay'] for _, _, d in G.edges(data=True)])
    avg_loss  = np.mean([d['loss']  for _, _, d in G.edges(data=True)])
    avg_util  = np.mean([d['utilization'] for _, _, d in G.edges(data=True)])
    print(f"  Avg link: delay={avg_delay:.1f}ms  loss={avg_loss*100:.2f}%  "
          f"util={avg_util*100:.0f}%")
    return G


def _rand_link():
    """Generate link with correlated properties (realistic model)."""
    bw = random.choice([10, 100, 1000])
    if bw == 1000:          # Fibre: fast, clean, but popular → congested
        delay = random.uniform(1, 10)
        loss  = random.uniform(0, 0.02)
        util  = random.uniform(0.50, 0.95)
    elif bw == 100:         # Copper/mixed: moderate on all dimensions
        delay = random.uniform(5, 30)
        loss  = random.uniform(0.01, 0.05)
        util  = random.uniform(0.20, 0.70)
    else:                   # 10 Mbps wireless/legacy: high delay+loss, low util
        delay = random.uniform(15, 50)
        loss  = random.uniform(0.03, 0.08)
        util  = random.uniform(0.05, 0.40)
    return dict(bandwidth=bw, delay=delay, loss=loss, utilization=util)


# ════════════════════════════════════════════════════════════
# 2. METRIC FUNCTIONS
# ════════════════════════════════════════════════════════════

def rip_cost(G, u, v):
    """RIP: hop count — always 1 regardless of link quality."""
    return 1

def ospf_cost(G, u, v):
    """OSPF: inverse bandwidth — 100,000 / BW(Mbps).
    Ignores delay, utilisation, and loss."""
    return max(1, int(100000 / G[u][v]['bandwidth']))

def qaa_ospf_cost(G, u, v):
    """QAA-OSPF: 3-component normalized composite metric.
    M = 0.40·C_bw + 0.35·C_delay + 0.25·C_load
    All components normalized to 0-100 so weights are meaningful.
    """
    d = G[u][v]
    c_bw    = max(0, 100 - (math.log10(d['bandwidth']) - 1) * 50)
    c_delay = (d['delay'] / 50) * 100
    c_load  = d['utilization'] * 100
    return 0.40 * c_bw + 0.35 * c_delay + 0.25 * c_load

def eqa_ospf_cost(G, u, v):
    """EQA-OSPF: 4-component normalized composite metric.
    M = 0.35·C_bw + 0.30·C_delay + 0.20·C_load + 0.15·C_loss
    Adds packet-loss term absent in QAA-OSPF, enabling loss-aware routing.
    """
    d = G[u][v]
    c_bw    = max(0, 100 - (math.log10(d['bandwidth']) - 1) * 50)
    c_delay = (d['delay'] / 50) * 100
    c_load  = d['utilization'] * 100
    c_loss  = (d['loss'] / 0.08) * 100
    return 0.35 * c_bw + 0.30 * c_delay + 0.20 * c_load + 0.15 * c_loss


# ════════════════════════════════════════════════════════════
# 3. DIJKSTRA (OSPF, QAA-OSPF, EQA-OSPF)
# ════════════════════════════════════════════════════════════

def dijkstra_all(G, source, cost_fn):
    """Run Dijkstra from source using cost_fn(G, u, v).
    Returns {dest: (cost, path)} for all destinations."""
    dist = {n: math.inf for n in G.nodes()}
    prev = {n: None     for n in G.nodes()}
    dist[source] = 0
    visited = set()
    queue = [(0, source)]

    while queue:
        d, u = heapq.heappop(queue)
        if u in visited:
            continue
        visited.add(u)
        for v in G.neighbors(u):
            w = cost_fn(G, u, v)
            if d + w < dist[v]:
                dist[v] = d + w
                prev[v] = u
                heapq.heappush(queue, (dist[v], v))

    result = {}
    for dest in G.nodes():
        if dest == source:
            continue
        if math.isinf(dist[dest]):
            result[dest] = (math.inf, [])
        else:
            path, cur = [], dest
            seen = set()
            while cur is not None and cur not in seen:
                path.append(cur)
                seen.add(cur)
                cur = prev[cur]
            result[dest] = (dist[dest], list(reversed(path)))
    return result


# ════════════════════════════════════════════════════════════
# 4. BELLMAN-FORD (RIP)
# ════════════════════════════════════════════════════════════

def bellman_ford_all(G, source, max_hops=15):
    """Bellman-Ford with RIP's 15-hop limit.
    Returns {dest: (cost, path)}."""
    dist = {n: math.inf for n in G.nodes()}
    prev = {n: None     for n in G.nodes()}
    hops = {n: math.inf for n in G.nodes()}
    dist[source] = 0
    hops[source] = 0

    for _ in range(len(G.nodes()) - 1):
        updated = False
        for u, v in G.edges():
            for a, b in [(u, v), (v, u)]:
                if dist[a] + 1 < dist[b] and hops[a] + 1 <= max_hops:
                    dist[b] = dist[a] + 1
                    prev[b] = a
                    hops[b] = hops[a] + 1
                    updated = True
        if not updated:
            break

    result = {}
    for dest in G.nodes():
        if dest == source:
            continue
        if math.isinf(dist[dest]):
            result[dest] = (math.inf, [])
        else:
            path, cur = [], dest
            seen = set()
            while cur is not None and cur not in seen:
                path.append(cur)
                seen.add(cur)
                cur = prev[cur]
            result[dest] = (dist[dest], list(reversed(path)))
    return result


# ════════════════════════════════════════════════════════════
# 5. PATH QUALITY ANALYSIS
# ════════════════════════════════════════════════════════════

def measure_path_quality(G, cost_fn, n_flows=200, use_bf=False):
    """Measure actual latency and loss on protocol-selected paths.
    Uses deterministic src/dst pairs so all protocols are compared fairly.
    Actual link attributes (delay, loss) are measured — not the routing metric.
    """
    nodes = list(G.nodes())
    latencies, losses, hop_counts = [], [], []

    for i in range(n_flows):
        src = (i * 7 + 3) % len(nodes)
        dst = (i * 11 + 5) % len(nodes)
        if dst == src:
            dst = (dst + 1) % len(nodes)

        if use_bf:
            table = bellman_ford_all(G, src)
        else:
            table = dijkstra_all(G, src, cost_fn)

        if dst in table and not math.isinf(table[dst][0]):
            path = table[dst][1]
            if len(path) < 2:
                continue
            # Sum actual link delays along chosen path
            lat = sum(G[path[j]][path[j+1]]['delay']
                      for j in range(len(path)-1))
            # Compound loss probability
            prob = 1.0
            for j in range(len(path)-1):
                prob *= (1 - G[path[j]][path[j+1]]['loss'])
            latencies.append(lat)
            losses.append((1 - prob) * 100)
            hop_counts.append(len(path) - 1)

    return {
        'avg_latency': np.mean(latencies)   if latencies else 0,
        'avg_loss':    np.mean(losses)       if losses    else 0,
        'avg_hops':    np.mean(hop_counts)   if hop_counts else 0,
        'p95_latency': np.percentile(latencies, 95) if latencies else 0,
        'p95_loss':    np.percentile(losses,   95) if losses    else 0,
        'delivered':   len(latencies),
        'attempted':   n_flows,
    }


# ════════════════════════════════════════════════════════════
# 6. CONVERGENCE TIME SIMULATION
#    Protocol timers scaled for simulation (real values in comments).
#    RIP:      Hello 30 s, Dead 180 s  → sim 3 s / 9 s
#    OSPF:     Hello 10 s, Dead 40 s   → sim 1 s / 4 s
#    QAA-OSPF: same as OSPF + backup
#    EQA-OSPF: BFD 50 ms × 3 = 150 ms + instant backup
# ════════════════════════════════════════════════════════════

PROTO_PARAMS = {
    'RIP':      {'dead_interval': 9000,  'detect': 'hello_timeout'},
    'OSPF':     {'dead_interval': 4000,  'detect': 'hello_timeout', 'spf_delay': 50},
    'QAA-OSPF': {'dead_interval': 4000,  'detect': 'hello_timeout', 'spf_delay': 50},
    'EQA-OSPF': {'dead_interval': 4000,  'detect': 'bfd',
                 'bfd_interval': 50, 'bfd_multiplier': 3, 'spf_delay': 50},
}

def simulate_convergence(G, n_trials=30):
    """Simulate link-failure convergence for each protocol over n_trials.
    Increased to 30 trials for statistical stability."""
    results = {}
    edges = list(G.edges())

    for proto in ['RIP', 'OSPF', 'QAA-OSPF', 'EQA-OSPF']:
        params = PROTO_PARAMS[proto]
        times = []

        for _ in range(n_trials):
            # Detection time
            if params['detect'] == 'bfd':
                detect_ms = params['bfd_interval'] * params['bfd_multiplier']  # 150 ms deterministic
            else:
                detect_ms = random.uniform(
                    params['dead_interval'] * 0.5,
                    params['dead_interval']
                )

            # Propagation (SPF + LSA flood)
            if proto == 'RIP':
                propagation_ms = random.uniform(100, 500)
            else:
                spf_ms   = params.get('spf_delay', 50)
                flood_ms = random.uniform(10, 50)
                propagation_ms = spf_ms + flood_ms

            # Total convergence
            if proto == 'EQA-OSPF':
                # BFD detects in 150 ms; backup activates in ~5 ms
                conv_time = detect_ms + 5
            elif proto == 'QAA-OSPF':
                # Pre-computed backup halves propagation overhead
                conv_time = detect_ms + propagation_ms * 0.5
            else:
                conv_time = detect_ms + propagation_ms

            times.append(conv_time)

        results[proto] = {
            'times': times,
            'mean':  np.mean(times),
            'std':   np.std(times),
            'min':   np.min(times),
            'max':   np.max(times),
        }

    return results


