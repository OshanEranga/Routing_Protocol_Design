# 9. LARGE-SCALE SCALABILITY TEST
# ════════════════════════════════════════════════════════════

def large_scale_test(sizes=None):
    """Test protocols on progressively larger networks.
    RIP is skipped above 30 nodes (hop limit makes it impractical).
    """
    if sizes is None:
        sizes = [20, 50, 100]

    protocols = {
        'RIP':      (rip_cost,      True),
        'OSPF':     (ospf_cost,     False),
        'QAA-OSPF': (qaa_ospf_cost, False),
        'EQA-OSPF': (eqa_ospf_cost, False),
    }
    results = []

    for size in sizes:
        print(f"\n  --- Network: {size} nodes ---")
        G_s = build_topology(size, seed=42 + size)
        row = {'size': size, 'edges': G_s.number_of_edges()}

        for name, (cost_fn, use_bf) in protocols.items():
            if name == 'RIP' and size > 30:
                print(f"    [{name}] SKIPPED (15-hop limit unworkable at {size} nodes)")
                row[name] = {'skipped': True}
                continue

            t0 = time.perf_counter()
            if use_bf:
                table = bellman_ford_all(G_s, 0)
            else:
                table = dijkstra_all(G_s, 0, cost_fn)
            comp_time = (time.perf_counter() - t0) * 1000

            valid = sum(1 for d, (c, _) in table.items() if not math.isinf(c))
            quality = measure_path_quality(
                G_s, cost_fn, n_flows=min(50, size * 2), use_bf=use_bf
            )

            print(f"    [{name}] comp={comp_time:.1f}ms  routes={valid}/{size-1}  "
                  f"lat={quality['avg_latency']:.1f}ms  loss={quality['avg_loss']:.2f}%")

            row[name] = {
                'comp_time':   comp_time,
                'valid_routes': valid,
                'quality':     quality,
                'skipped':     False,
            }

        results.append(row)

    return results


